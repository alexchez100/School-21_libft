#include "stdlib.h"

int		ft_isalnum(int ch)
{
	if (ch > 64 && ch < 91)
		return (1);
	if (ch > 96 && ch < 123)
		return (2);
	if (ch > 47 && ch < 58)
		return (4);
	return (0);
}
